void mx_printint(int n) {
    char buffer[12]; // Максимум 11 символов для -2147483648 и '\0'
    int i = 0;

    if (n == 0) { // Обрабатываем случай, если n = 0
        mx_printchar('0');
        return;
    }

    if (n < 0) { // Если число отрицательное
        mx_printchar('-');
        n = -n;
    }

    // Преобразуем число в символы, начиная с конца
    while (n > 0) {
        buffer[i++] = (n % 10) + '0'; // Добавляем последнюю цифру в массив
        n /= 10;                     // Убираем последнюю цифру
    }

    // Выводим массив в обратном порядке
    for (int j = i - 1; j >= 0; j--) {
        mx_printchar(buffer[j]);
    }
}
